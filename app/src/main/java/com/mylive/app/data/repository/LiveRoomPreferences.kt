        danmuTopMargin = preferences[SettingsDataStore.DanmuTopMargin] ?: 0.0,
        danmuBottomMargin = preferences[SettingsDataStore.DanmuBottomMargin] ?: 0.0,
        danmuDedupeEnable = preferences[SettingsDataStore.DanmuDedupeEnable] ?: false,
        danmuDedupeWindow = preferences[SettingsDataStore.DanmuDedupeWindow] ?: 10,

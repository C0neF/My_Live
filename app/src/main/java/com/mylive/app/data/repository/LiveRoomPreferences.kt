        danmuLineCount = preferences[SettingsDataStore.DanmuLineCount] ?: 8,
        danmuDelay = preferences[SettingsDataStore.DanmuDelay] ?: 0.0,
        danmuDelayBySiteJson = preferences[SettingsDataStore.DanmuDelayBySite] ?: "{}",
        danmuOpacity = preferences[SettingsDataStore.DanmuOpacity] ?: 1.0,
